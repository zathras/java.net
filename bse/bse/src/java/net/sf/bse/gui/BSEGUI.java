    private void xletSignButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_xletSignButtonActionPerformed
    {//GEN-HEADEREND:event_xletSignButtonActionPerformed
        java.io.File destDir = 
            new java.io.File(xletDestinationDirTextField.getText());
        if (destDir.exists() && !xletDeleteDestDirCheckBox.isSelected())
        {
            int option = javax.swing.JOptionPane.showConfirmDialog(this, 
                "The destination directory exists and will be removed.\n" +
                "Do you want to continue?", 
                "Remove Existing Directory?", 
                javax.swing.JOptionPane.YES_NO_OPTION);
            if (option == javax.swing.JOptionPane.NO_OPTION)
            {
                return;
            }
        }
        
        Object[] certFiles = 
            ((javax.swing.DefaultListModel)xletCertList.getModel()).toArray();
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < certFiles.length; i++)
        {
            sb.append(((java.io.File)certFiles[i]).getAbsolutePath());
            if (i < certFiles.length - 1)
            {
                sb.append(java.io.File.pathSeparatorChar);
            }            
        }
        String certs = sb.toString();
        
        String[] signFileNames = xletTree.getSignFileNames();        
        sb = new StringBuffer();
        for (int i = 0; i < signFileNames.length; i++)
        {
            sb.append(signFileNames[i]);
            if (i < signFileNames.length - 1)
            {
                sb.append(java.io.File.pathSeparatorChar);
            }
        }
        String files = sb.toString();
                
        java.util.HashMap arguments = new java.util.HashMap();

        arguments.put("certs:", certs);
        arguments.put("key:", xletPrivateKeyFileTextField.getText());
        arguments.put("src:", xletTree.getRoot().getAbsolutePath());
        arguments.put("dest:", xletDestinationDirTextField.getText());
        arguments.put("files:", files);
        arguments.put("rm:", "true");
        
        net.sf.bse.Command command = new net.sf.bse.SignXlet(arguments);
        try
        {
            command.run();
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }                
    }//GEN-LAST:event_xletSignButtonActionPerformed

