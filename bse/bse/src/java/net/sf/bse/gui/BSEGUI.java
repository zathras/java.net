    private void xletCertDownButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_xletCertDownButtonActionPerformed
    {//GEN-HEADEREND:event_xletCertDownButtonActionPerformed
        int index = xletCertList.getSelectedIndex();
        javax.swing.DefaultListModel model = 
            (javax.swing.DefaultListModel)xletCertList.getModel();
        if (index < model.size() - 1)
        {
            model.add(index + 1, model.remove(index));
            xletCertList.setSelectedIndex(index + 1);
        }
        else
        {
            xletCertList.setSelectedIndex(index);
        }        
    }//GEN-LAST:event_xletCertDownButtonActionPerformed

